package com.keyin.qap1;

// Simple calculator app that we shall use over in CalculatorTest.java
public class Calculator {
    // Addition
    public int add(int x, int y) {
        return x + y;
    }
    // Subtraction
    public int subtract(int x, int y) {
        return x - y;
    }
    // Multiplication
    public long multiply(int x, int y) {
        return x * y;
    }
    // Division
    public long divide(int x, int y) {
        return x / y;
    }
}